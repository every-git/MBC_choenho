-- 가격대별 상품 수
SELECT 
    CASE 
        WHEN price < 10000 THEN '1만원 미만'
        WHEN price < 20000 THEN '1만원대'
        WHEN price < 30000 THEN '2만원대'
        ELSE '3만원 이상'
    END AS '가격대',
    COUNT(*) AS '상품 수'
FROM product
GROUP BY 
    CASE 
        WHEN price < 10000 THEN '1만원 미만'
        WHEN price < 20000 THEN '1만원대'
        WHEN price < 30000 THEN '2만원대'
        ELSE '3만원 이상'
    END
ORDER BY MIN(price);

