public class ForEx04 {
// 구구단을 for문을 사용해서 출력하는 코드입니다.
// 2단을 출력하는 코드입니다.
// 코드를 설명하면 다음과 같습니다.

	public static void main(String[] args) {
		for (int i = 1; i <= 9; i++) {
				System.out.println(2 + " * " + i + " = " + 2 * i);
		}
		System.out.println();
		System.out.println("-----2단 출력 완료-----");
	}

}
